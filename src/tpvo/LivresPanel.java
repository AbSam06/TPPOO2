    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
         try {
        // Récupérer le texte de recherche depuis le champ de texte
        String titreRecherche = jTextField7.getText();

        // Appeler la méthode rechercherLivre
        GestionLivres gestionLivres = new GestionLivres();
        List<Object[]> livres = gestionLivres.rechercherLivre(titreRecherche);

        // Nettoyer le modèle de table
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        // Ajouter les livres trouvés au modèle de table
        for (Object[] livre : livres) {
            model.addRow(livre);
        }

        // Vérifiez si aucun livre n'a été trouvé
        if (livres.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Aucun livre trouvé pour ce titre.", "Information", JOptionPane.INFORMATION_MESSAGE);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Une erreur s'est produite : " + e.getMessage(), "Erreur", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton4ActionPerformed


