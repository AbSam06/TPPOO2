        // Message de confirmation
        JOptionPane.showMessageDialog(this, "Livre ajouté avec succès !");

        // Réinitialiser les champs
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText("");
        jTextField4.setText("");
        jTextField5.setText("");
        jTextField6.setText("");

