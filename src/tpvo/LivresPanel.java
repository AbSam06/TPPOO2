    private void chargerLivres() {
    try {
        // Récupérer les données des livres
        GestionLivres gestionLivres = new GestionLivres();
        List<Object[]> livres = gestionLivres.lireLivres();

        // Nettoyer le modèle de table
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Efface toutes les lignes existantes

        // Ajouter les livres au modèle de table
        for (Object[] livre : livres) {
            model.addRow(livre);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erreur lors du chargement des livres : " + e.getMessage(), "Erreur", JOptionPane.ERROR_MESSAGE);
    }
}
    
    
    
    private void remplirTextFieldsDepuisTable() {
    // Récupérer l'index de la ligne sélectionnée
    int selectedRow = jTable1.getSelectedRow();
    if (selectedRow != -1) {
        // Récupérer les valeurs de la ligne sélectionnée
        jTextField1.setText(jTable1.getValueAt(selectedRow, 0).toString()); // ID
        jTextField2.setText(jTable1.getValueAt(selectedRow, 1).toString()); // Titre
        jTextField3.setText(jTable1.getValueAt(selectedRow, 2).toString()); // Année
        jTextField4.setText(jTable1.getValueAt(selectedRow, 3).toString()); // ISBN
        jTextField5.setText(jTable1.getValueAt(selectedRow, 4).toString()); // ID_Éditeur
        jTextField6.setText(jTable1.getValueAt(selectedRow, 5).toString()); // ID_Catégorie
    }
}
    
